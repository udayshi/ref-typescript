// Type Inference
let inferredString = "Hello, TypeScript!"; // TypeScript infers the type as string
console.log("Inferred string:", inferredString); // Output: Inferred string: Hello, TypeScript!

let inferredNumber = 42; // TypeScript infers the type as number
console.log("Inferred number:", inferredNumber); // Output: Inferred number: 42

let inferredBoolean = true; // TypeScript infers the type as boolean
console.log("Inferred boolean:", inferredBoolean); // Output: Inferred boolean: true

// Type Annotations
let annotatedString: string = "Explicit string type";
console.log("Annotated string:", annotatedString); // Output: Annotated string: Explicit string type

let annotatedNumber: number = 100;
console.log("Annotated number:", annotatedNumber); // Output: Annotated number: 100

let annotatedBoolean: boolean = false;
console.log("Annotated boolean:", annotatedBoolean); // Output: Annotated boolean: false